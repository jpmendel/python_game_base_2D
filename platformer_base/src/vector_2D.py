# Vector2D Class

class Vector2D:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def add(vector):
        self.x += vector.x
        self.y += vector.y

    def sub(vector):
        self.x -= vector.x
        self.y -= vector.y

    def mult(vector):
        self.x *= vector.x
        self.y *= vector.y
        
    def div(vector):
        self.x /= vector.x
        self.y /= vector.y
